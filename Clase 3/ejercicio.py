# solicitar teclado cantidad de producto, si el producto es menor 0 indicar fuera de stock
# si la cantidad es mayor a0 indicar hay stock
stock = int(input("Ingresa la cantidad disponible del producto: "))
if stock > 0:
    print("✔ Hay stock!")
else:
    print("❌Producto agotado!") #emojis tecla inicio + .

    